class Solution:
    def solve(self, board: List[List[str]]) -> None:
        if not board or not board[0]:
            return
        
        m, n = len(board), len(board[0])
        
        def dfs(r, c):
            if r < 0 or c < 0 or r >= m or c >= n or board[r][c] != 'O':
                return
            board[r][c] = 'T'
            dfs(r + 1, c)
            dfs(r - 1, c)
            dfs(r, c + 1)
            dfs(r, c - 1)
        
        # Step 1: Mark unsubstantiated 'O's on the borders
        for i in range(m):
            dfs(i, 0)
            dfs(i, n - 1)
            
        for j in range(n):
            dfs(0, j)
            dfs(m - 1, j)
            
        # Step 2: Flip captured 'O's to 'X's, and restore escaped 'O's
        for r in range(m):
            for c in range(n):
                if board[r][c] == 'O':
                    board[r][c] = 'X'
                elif board[r][c] == 'T':
                    board[r][c] = 'O'